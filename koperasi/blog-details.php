                <h4>Kemenkop Dukung Lahirnya Industri Kreatif Perfilman Berbadan Hukum Koperasi</h4>

                <p><i class="fa fa-user"></i> Mahasiswa &nbsp;|&nbsp; <i class="fa fa-calendar"></i> 21.06.2021 10:10 &nbsp;|&nbsp; <i class="fa fa-comments"></i>  15 comments</p>

                <div><img src="assets/images/blog-image-fullscren-1-1920x700.jpg" alt=""></div>

                <br>

                <p> Kementerian Koperasi (Kemenkop) dan UKM mendukung para pelaku industri kreatif di bidang film khususnya yang berbadan hukum koperasi untuk tumbuh dan berkembang.</p>

                <p>“Hal ini agar dapat menjadi media yang tepat menyampaikan pesan kepada generasi milenial tentang nilai-nilai budaya Indonesia yang bercirikan kolaborasi, bukan kompetisi,” kata Deputi Kelembagaan Kementerian Koperasi dan UKM Rully Indrawan, Senin (21/1/2019).</p>

                <p>Film merupakan salah satu media menyampaikan informasi kepada generasi millenial saat ini. Film adalah media yang tepat untuk menyampaikan pesan-pesan moral melalui budaya kerja sama.</p>

                <p>Salah satu progammer film-film industri UKM kreatif adalah Infoscreening yang berbadan hukum Koperasi Tri Sakti Bhakti Pertiwi sebagai konsultan CGV yang menayangkan film dengan judul “Kucumbu Tubuh Indahmu” karya sutradara ternama Garin Nugroho.</p>

                <p>Pada prinsipnya dalam proses pembuatan film, imbuhnya, diperlukan kolaborasi antar berbagai insan kreatif. Kemenkop dan UKM mendukung tumbuhnya wirausaha di industri film yang merupakan sub sektor industri kreatif.</p>

                <p>"Pelaku industri kreatif film ini termasuk pelaku UMKM, wirausaha kreatif. Koperasi juga perlu disampaikan melalui media film kepada anak millenial, saat ini," papar Rully.</p>

                <p>Yang jelas, harus diapresiasi atas lahirnya koperasi jenis baru yang sangat kreatif ini. Ini merupakan warna baru, terobosan baru bagi koperasi dan inspirasi baru bagi kawula muda milenial, "semoga ini merupakan langkah yang sangat baik juga bagi perkembangan insan perfilman di tanah air," ujarnya berharap.</p>

                <p>Rully juga berharap, langkah ini segera diikuti oleh komunitas, komunitas yang lain, baik seni rupa, musik maupun yang lainnya dengan menggunakan badan hukum koperasi.</p>

                <p>Di tempat yang sama sutradara “Kucumbu Tubuh Indahmu” Garin Nugroho menyambut baik keterlibatan koperasi dalam insan perfilman. Koperasi dan UKM, harapnya, harus berjalan sebanding dengan tumbuhnya komunitas.</p>

                <p>“Komuniti ini adalah dasar daya hidup koperasi. Dan era film saat ini adalah era film komunitas, sehingga sudah tepat koperasi tumbuh bersama komunitas film dengan menggunakan teknologi digital tepat guna, sehingga bisa tumbuh di wilayah-wilayah komunitas yang menjadi dasar hidup koperasi,” papar Garin.</p>

